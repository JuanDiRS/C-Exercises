//Realizaremos una tarea simple la cual sera mostrar numeros del 1 al 4 pero lo haremos de manera muy simople y de tres maneras diferentes segun las siguientes condiciones:

//- Con una instruccion de salida y un operador de insercion de flujo.
#include <iostream>
int main()
{
    std::cout<<"1 2 3 4";
}
//- Con una sola instruccion de salida y cuatro operadores de insercion de flujo.
#include <iostream>
int main()
{
    std::cout<<"1 "<<"2 "<<"3 "<<"4";
}
//Con cuatro instrucciones de salida.
#include <iostream>
int main()
{
    std::cout<<"1 ";
    std::cout<<"2 ";
    std::cout<<"3 ";
    std::cout<<"4";
}
